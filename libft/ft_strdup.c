#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t	i;
	char	*src;
	char	*dest;

	i = 0;
	src = (char *)s1;
	i = ft_strlen(src);
	dest = (char *)malloc((i + 1) * sizeof(char));
	if (!dest || !src)
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
